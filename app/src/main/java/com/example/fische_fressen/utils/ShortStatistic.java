package com.example.fische_fressen.utils;

public class ShortStatistic {
    private int score;
    private int id;

    public ShortStatistic(int score, int id){
        this.score = score;
        this.id = id;
    }

    public int getScore() {
        return score;
    }

    public int getId() {
        return id;
    }

}
